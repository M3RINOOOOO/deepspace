    void setDamage(DamageToUI damage){
        if(damage == null){
            setVisible(false);
        }
        else{
            armas.setText(damage.getWeaponInfo());
            escudos.setText(Integer.toString(damage.getNShields()));
            repaint();
            revalidate();
        }
    }
    
